// Returns the collision value given a global position on the map
function checkMapCollision(mapID, posX, posY) {

    // Check if we are off the map all together
    if (posX < 0 || posX > tileSize * mapWidth) {
        return 0;
    }
    if (posY > 0 || posY < -tileSize * mapHeight) {
        return 0;
    }

    // Get the tile from the map
    let mapY = floor(-posY / tileSize);
    let mapX = floor(posX / tileSize);

    let tileID = mapTiles[mapID][mapY * mapWidth + mapX];
    if (tileID == 0 || tileID == 10) { return 0; } // All points on tile 0 and 10 are 0, so skip next step
    if (tileID == 9) { return 1; } // All points on tile 9 are 1, so skip next step

    // Get the position on this tile
    posX -= mapX * tileSize;
    posY += mapY * tileSize;

    let tileX = floor(posX / tileSize * collisionSize);
    let tileY = floor(-posY / tileSize * collisionSize);

    // Index this tile and return the tile collision value
    return tileCollisions[tileID][tileY * collisionSize + tileX];

}

